=== Pre-requisites===
<PRE>
Some or all of following pre-requisites are needed in order to build or run the '''Umbraco 5''' source.
In many cases it's unpractical or not permitted for us to distribute the libraries or installers:

<PRE>
'''.NET 4.0'''

'''ASP.NET MVC3''' (RC2)[http://www.asp.net/mvc/mvc3]

'''Microsoft Research Code Contracts''' - [http://research.microsoft.com/en-us/projects/contracts/]

'''Microsoft Research Pex and Moles''' - helpers for unit testing (generates unit tests and allows 
replacement of .NET methods with delegates)[http://research.microsoft.com/en-us/projects/pex/downloads.aspx]

'''Sandcastle''' - Documentation Compiler for Managed Class Libraries [http://sandcastle.codeplex.com/]

'''Sandcastle Builder''' - GUI to help compose and build Sandcastle output [http://shfb.codeplex.com/]

Sandcastle June 2010 (2.6.10621.1) Patch - Rev 1 [http://sandcastlestyles.codeplex.com/releases/view/47767]

Jan 2010
'''Sandboxes/Hive/Hive.sln''' has been modified to use SqlCe4 in its tests

