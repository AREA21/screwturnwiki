summary: Bugetshop, die door de afnemer helemaal zelf is in te delen.

== functionaliteit==
* url: {subdomain}.sjop.nl
* keuze uit 10 designs
* de mogelijkheid zelf een logo te plaatsen (wordt automatich op maat gemaakt)
* verkoop tot 500 producten
* producten kunnen onderverdeeld worden in groepen<sup>(Catalogus)</sup>
* betaling vis iDEAL of onder Rembours
* 5 algemene pagina's, zelf in te delen.<sup>(EWE-Easy Web Edit)</sup>
* Klanten loggen in
* zoekmachine <sup>(Full Text Search)</sup>
* verzendkosten moeten berekend worden <sup>(Shiping cost)</sup>
* tegen meerprijs is een eigen domeinnaam mogelijk<sup>(CNAME pointer)</sup>
* betaling op factuur 52,50 p.m.
* Adonnementsduur 1 jaar, met een opzegtermijn van een maand
* afbeeldingen worden op maat gemaakt.
* 6 producten in focus op starpagina.

