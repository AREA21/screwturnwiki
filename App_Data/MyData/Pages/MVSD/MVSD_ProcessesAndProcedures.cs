=== Administratieve procedure acceptatie webbouw ===
<pre>
'''Webbouw'''
* Reclamebureau levert ontwerp aan.
* MVSD geeft aan in hoeverre dit in een CMS te zetten is, in beeld en geschrift. MVSD zet dit in een functionele specificatie.
* Reclamebureau tekent dit voor akkoord.
* MVSD plaatst de website op de testserver, zodat het reclamebureau kan testen.
* Het reclamebureau tekent het testformulier en tekent hiermee voor het live zetten van de site.
* MVSD zet de site live.
</pre>
<pre>
'''Hosting'''
* Klant geeft akkoord voor hosting.
* Klant geeft aan wat hij wil, een pointer naar de MVSD server of een domeinverhuizing.
* Bij domeinverhuizing geeft de klant aan hoe de email is geregeld.
</pre>
